/// <summary>
/// Defines the layout modes for the Ribbon control.
/// </summary>
public enum RibbonLayoutMode
{
    /// <summary>
    /// Classic ribbon layout (3 rows, full height).
    /// Traditional Office ribbon with large icons and text labels.
    /// </summary>
    Classic,

    /// <summary>
    /// Simplified ribbon layout (1 row, compact).
    /// Modern Office ribbon with small icons and minimal labels.
    /// </summary>
    Simplified
}
